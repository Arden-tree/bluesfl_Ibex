import matplotlib.pyplot as plt


def print_statistics(hit_count, top_10_bids):
    not_top10_but_hit_count = 0
    not_top10_but_not_hit_count = 0
    not_top10_total = 0

    for bid in top_10_bids:
        assert bid in hit_count
        assert hit_count[bid] == 1

    for bid in hit_count:
        if bid not in top_10_bids:
            if hit_count[bid] == 1:
                not_top10_but_hit_count += 1
            else:
                not_top10_but_not_hit_count += 1
            not_top10_total += 1

    assert not_top10_total == (len(hit_count) - len(top_10_bids))

    in_top10 = len(top_10_bids)
    print(f"in top_10: {in_top10}")

    print(
        f"not in top_10, but it hit in trace: "
        f"{not_top10_but_hit_count}, "
        f"{not_top10_but_hit_count * 1. / not_top10_total * 100:.2f}%"
    )

    print(
        f"not in top_10, and it not hit in trace: "
        f"{not_top10_but_not_hit_count}, "
        f"{not_top10_but_not_hit_count * 1. / not_top10_total * 100:.2f}%"
    )

    return in_top10, not_top10_but_hit_count, not_top10_but_not_hit_count


